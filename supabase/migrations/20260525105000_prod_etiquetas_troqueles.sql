create table if not exists public.prod_etiquetas_troqueles (
  id bigint generated by default as identity primary key,
  
  -- Identificación
  codigo text not null unique,
  carpeta_original text not null,
  carpeta_path text,
  
  -- Dimensiones y forma
  forma text,
  ancho_mm numeric,
  alto_mm numeric,
  diametro_mm numeric,
  dimensiones_texto text,
  
  -- Características especiales
  especial boolean default false,
  multiple boolean default false,
  con_hendido boolean default false,
  
  -- Cliente y trabajo
  cliente text,
  trabajo text,
  
  -- Estado y gestión
  estado text default 'activo',
  necesita_revision boolean default false,
  notas text,
  
  -- Documentos asociados (JSON array)
  documentos jsonb,
  
  -- Timestamps
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

