-- Trigger para actualizar updated_at
create or replace function public.update_prod_etiquetas_troqueles_updated_at()
returns trigger as $$
begin
  new.updated_at = now();
  return new;
end;
$$ language plpgsql;

drop trigger if exists prod_etiquetas_troqueles_updated_at_trigger 
  on public.prod_etiquetas_troqueles;

create trigger prod_etiquetas_troqueles_updated_at_trigger
  before update on public.prod_etiquetas_troqueles
  for each row
  execute function public.update_prod_etiquetas_troqueles_updated_at();

